package modelo;

/**
 *
 * @author Carlos
 */
public class ServicioPaquete extends Servicio{
    private int desde;
    private int hasta;

    public ServicioPaquete(int desde, int hasta) {
        this.desde = desde;
        this.hasta = hasta;
    }

    public int getDesde() {
        return desde;
    }

    public void setDesde(int desde) {
        this.desde = desde;
    }

    public int getHasta() {
        return hasta;
    }

    public void setHasta(int hasta) {
        this.hasta = hasta;
    }
}
